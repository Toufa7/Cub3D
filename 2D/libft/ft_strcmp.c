#include"libft.h"

int	ft_strcmp(char	*s1, char	*s2)
{
	if (!s1 || !s2)
	{
		printf("STRCMP \n");
		return (1);
	}
	while (*s1 != '\0' && *s2 != '\0' && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
